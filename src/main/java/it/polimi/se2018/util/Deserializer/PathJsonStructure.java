package it.polimi.se2018.util.Deserializer;

public class PathJsonStructure {

    String type;
    String path;

    public void setType(String type) {
        this.type = type;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getType() {
        return type;
    }

    public String getPath() {
        return path;
    }
}
